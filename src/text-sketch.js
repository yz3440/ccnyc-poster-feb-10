// Parse SVG path to get bounding box
function getPathBounds(pathStr) {
  // Extract all numbers from path
  const numbers = pathStr.match(/-?\d+\.?\d*/g)?.map(Number) || [];
  if (numbers.length < 2) return { minX: 0, minY: 0, maxX: 0, maxY: 0 };
  
  let minX = Infinity, minY = Infinity, maxX = -Infinity, maxY = -Infinity;
  
  // Simple parsing - assumes coordinates come in pairs
  for (let i = 0; i < numbers.length - 1; i += 2) {
    const x = numbers[i];
    const y = numbers[i + 1];
    if (x < 1500 && y < 2000 && x > 0 && y > 0) { // Filter reasonable coords
      minX = Math.min(minX, x);
      maxX = Math.max(maxX, x);
      minY = Math.min(minY, y);
      maxY = Math.max(maxY, y);
    }
  }
  
  return { minX, minY, maxX, maxY };
}

// Pre-calculate letter centers and bounds
const letterData = LETTERS.map(letter => {
  const bounds = getPathBounds(letter.path);
  const centerX = (bounds.minX + bounds.maxX) / 2;
  const centerY = (bounds.minY + bounds.maxY) / 2;
  const width = bounds.maxX - bounds.minX;
  const height = bounds.maxY - bounds.minY;
  return {
    ...letter,
    bounds,
    centerX,
    centerY,
    width,
    height
  };
});

let pane;
let svgShapes = [];

const PARAMS = {
  rotationSpeed: 1.0,
  phaseMultiplier: 0.003, // How much diagonal position affects phase
  backgroundColor: { r: 20, g: 20, b: 20 },
  letterColor: { r: 255, g: 255, b: 255 },
  perspective: 800,
  thickness: 3, // Simulated 3D thickness
};

const sketch = (p) => {
  p.preload = () => {
    // Load SVG shapes
    for (const letter of letterData) {
      // Create a mini SVG for each letter
      const svgString = `<svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 1080 1920"><path d="${letter.path}" fill="white"/></svg>`;
      const blob = new Blob([svgString], { type: 'image/svg+xml' });
      const url = URL.createObjectURL(blob);
      const shape = p.loadImage(url);
      svgShapes.push({ ...letter, shape });
    }
  };

  p.setup = () => {
    const canvas = p.createCanvas(POSTER_WIDTH, POSTER_HEIGHT, p.WEBGL);
    canvas.parent("canvas-container");

    const maxHeight = window.innerHeight * 0.85;
    const scale = Math.min(1, maxHeight / POSTER_HEIGHT);
    canvas.style("width", `${POSTER_WIDTH * scale}px`);
    canvas.style("height", `${POSTER_HEIGHT * scale}px`);

    p.pixelDensity(1);
    p.frameRate(60);

    setupTweakpane();
    console.log("Text sketch ready. Letters:", letterData.length);
  };

  p.draw = () => {
    const { backgroundColor, letterColor, rotationSpeed, phaseMultiplier, perspective, thickness } = PARAMS;
    
    p.background(backgroundColor.r, backgroundColor.g, backgroundColor.b);
    
    // Set perspective
    p.perspective(p.PI / 3, POSTER_WIDTH / POSTER_HEIGHT, 0.1, 5000);
    
    const baseRotation = p.frameCount * rotationSpeed * 0.05;
    
    // Draw each letter
    for (const letter of svgShapes) {
      if (!letter.shape) continue;
      
      // Calculate diagonal phase offset based on center position
      const diagonalPos = letter.centerX + letter.centerY;
      const phaseOffset = diagonalPos * phaseMultiplier;
      const rotation = baseRotation + phaseOffset;
      
      p.push();
      
      // Move to letter center (convert from SVG coords to WEBGL coords)
      const x = letter.centerX - POSTER_WIDTH / 2;
      const y = letter.centerY - POSTER_HEIGHT / 2;
      p.translate(x, y, 0);
      
      // Rotate around Y axis (vertical axis)
      p.rotateY(rotation);
      
      // Draw the letter shape
      p.fill(letterColor.r, letterColor.g, letterColor.b);
      p.noStroke();
      
      // Draw front face
      p.push();
      p.translate(0, 0, thickness / 2);
      p.texture(letter.shape);
      p.plane(letter.width, letter.height);
      p.pop();
      
      // Draw back face
      p.push();
      p.translate(0, 0, -thickness / 2);
      p.rotateY(p.PI);
      p.texture(letter.shape);
      p.plane(letter.width, letter.height);
      p.pop();
      
      p.pop();
    }
  };

  p.windowResized = () => {
    const maxHeight = window.innerHeight * 0.85;
    const scale = Math.min(1, maxHeight / POSTER_HEIGHT);
    const canvas = document.querySelector("canvas");
    if (canvas) {
      canvas.style.width = `${POSTER_WIDTH * scale}px`;
      canvas.style.height = `${POSTER_HEIGHT * scale}px`;
    }
  };
};

